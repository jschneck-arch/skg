def generate_toolchain(
    domain: str,
    description: str,
    gap: dict,
    resonance_engine=None,
    staging_dir: Path | None = None,
    force: bool = False,
) -> dict:
    """
    Generate a complete toolchain for the given domain.

    Returns:
      {
        success: bool,
        domain: str,
        staging_path: str,
        catalog: dict,
        wicket_count: int,
        path_count: int,
        files_written: [str],
        errors: [str],
      }
    """
    staging_dir = staging_dir or FORGE_STAGING
    tc_name = f"skg-{domain}-toolchain"
    out_dir = staging_dir / tc_name

    if out_dir.exists() and not force:
        log.info(f"[forge] staging dir exists: {out_dir} (use force=True to regenerate)")
        return {
            "success": True, "domain": domain,
            "staging_path": str(out_dir),
            "already_existed": True,
        }

    if out_dir.exists():
        shutil.rmtree(out_dir)

    errors = []
    files_written = []

    # ----- Step 1: Generate catalog ----------------------------------------
    log.info(f"[forge] generating catalog for: {domain}")
    catalog = None
    generation_backend = None

    if resonance_engine:
        query = f"{domain}: {description} {gap.get('attack_surface','')}"
        context = resonance_engine.surface(query, k_each=4)
    else:
        context = {"wickets": [], "adapters": [], "domains": []}

    # Deterministic-first path: if the gap carries compiler hints from a fold or
    # gap detector, try to derive the catalog from CVE/NVD data before asking a
    # model to synthesize it.
    compiler_hints = gap.get("compiler_hints") or {}
    if compiler_hints:
        try:
            from skg.forge.compiler import compile_catalog
            compiled = compile_catalog(
                domain=domain,
                description=description,
                packages=list(compiler_hints.get("packages") or []),
                keywords=list(compiler_hints.get("keywords") or []),
                prefix=compiler_hints.get("prefix"),
                api_key=compiler_hints.get("api_key"),
                min_cvss=float(compiler_hints.get("min_cvss", 4.0)),
                max_wickets=int(compiler_hints.get("max_wickets", 20)),
            )
            if compiled and compiled.get("wickets"):
                catalog = compiled
                generation_backend = "compiler"
        except Exception as exc:
            errors.append(f"Compiler catalog generation failed: {exc}")

    # Try ollama first, then Anthropic API
    if catalog is None:
        try:
            from skg.resonance.ollama_backend import OllamaBackend
            backend = OllamaBackend()
            if backend.available():
                catalog, cat_errors = backend.draft_catalog(domain, description, context)
                errors.extend(cat_errors)
                generation_backend = f"ollama:{backend.model()}"
        except Exception as exc:
            log.debug(f"[forge] ollama unavailable: {exc}")

    if catalog is None:
        api_key = os.environ.get("ANTHROPIC_API_KEY")
        if api_key:
            try:
                from skg.resonance.drafter import draft_catalog
                # Build a minimal engine wrapper if needed
                class _MinEngine:
                    def surface(self, q, k_each=4): return context
                    def save_draft(self, d, c): return Path("/tmp") / f"{d}_draft.json"
                result = draft_catalog(_MinEngine(), domain, description, api_key=api_key)
                catalog = result["catalog"]
                errors.extend(result.get("validation_errors", []))
                generation_backend = "anthropic"
            except Exception as exc:
                errors.append(f"API catalog generation failed: {exc}")

    if catalog is None:
        # Last resort: generate a minimal catalog structure from the gap data
        catalog = _generate_minimal_catalog(domain, description, gap)
        generation_backend = "template"
        errors.append("Used template catalog — no generation backend available")

    # ----- Step 2: Generate adapter ----------------------------------------
    log.info(f"[forge] generating adapter for: {domain}")
    adapter_code = None
    example_adapters = _load_example_adapters(2)

    adapter_prompt = _build_adapter_prompt(
        domain, description, catalog, gap, context, example_adapters
    )

    if generation_backend and "ollama" in generation_backend:
        try:
            from skg.resonance.ollama_backend import OllamaBackend
            backend = OllamaBackend()
            raw = backend.generate(adapter_prompt, num_predict=1024)
            raw = re.sub(r'^```python\s*', '', raw)
            raw = re.sub(r'^```\s*', '', raw)
            raw = re.sub(r'\s*```$', '', raw).strip()
            if len(raw) > 200 and 'def ' in raw:
                adapter_code = _fix_adapter(raw, domain, catalog)
        except Exception as exc:
            errors.append(f"Adapter generation failed: {exc}")

    if adapter_code is None and os.environ.get("ANTHROPIC_API_KEY"):
        try:
            adapter_code = _generate_adapter_via_api(
                adapter_prompt, os.environ["ANTHROPIC_API_KEY"]
            )
            adapter_code = _fix_adapter(adapter_code, domain, catalog)
        except Exception as exc:
            errors.append(f"API adapter generation failed: {exc}")

    if adapter_code is None:
        adapter_code = _generate_template_adapter(domain, catalog, gap)
        errors.append("Used template adapter — check collection commands")

    # Quality gate: detect stub/TODO patterns — HARD REJECT.
    # A stub adapter is worse than no adapter: it silently produces wrong
    # wicket states that corrupt field inference.  If the LLM returned a stub,
    # retry once with an explicit anti-stub reinforcement prompt; if the retry
    # also fails, return an error result so the proposal is never staged.
    if adapter_code:
        _ok, _issues = _check_adapter_quality(adapter_code, domain)
        if not _ok:
            log.warning("[forge] adapter quality gate FAIL for %s — retrying with anti-stub prompt: %s",
                        domain, _issues)
            # One retry with stub rejection injected into prompt
            _retry_code = None
            try:
                _stub_hint = (
                    "\n\nPREVIOUS ATTEMPT WAS REJECTED by the quality gate.\n"
                    f"Issues found: {'; '.join(_issues)}\n"
                    "You MUST NOT use TODO, pass, raise NotImplementedError, or placeholder comments.\n"
                    "Every check function must contain real conditional logic that reads from `collection`.\n"
                )
                _retry_prompt = adapter_prompt + _stub_hint
                if generation_backend and "ollama" in generation_backend:
                    from skg.resonance.ollama_backend import OllamaBackend
                    _ob = OllamaBackend()
                    _rraw = _ob.generate(_retry_prompt, num_predict=1024)
                    _rraw = re.sub(r'^```python\s*', '', _rraw)
                    _rraw = re.sub(r'^```\s*', '', _rraw)
                    _rraw = re.sub(r'\s*```$', '', _rraw).strip()
                    if len(_rraw) > 200 and 'def ' in _rraw:
                        _retry_code = _fix_adapter(_rraw, domain, catalog)
                elif os.environ.get("ANTHROPIC_API_KEY"):
                    _rraw2 = _generate_adapter_via_api(_retry_prompt, os.environ["ANTHROPIC_API_KEY"])
                    _retry_code = _fix_adapter(_rraw2, domain, catalog)
            except Exception as _re:
                log.debug("[forge] adapter retry exception: %s", _re)

            if _retry_code:
                _ok2, _issues2 = _check_adapter_quality(_retry_code, domain)
                if _ok2:
                    adapter_code = _retry_code
                    log.info("[forge] adapter retry passed quality gate for %s", domain)
                else:
                    log.error("[forge] adapter retry still failed quality gate for %s: %s", domain, _issues2)
                    errors.extend([f"adapter_quality_hard_fail: {i}" for i in _issues2])
                    return {
                        "id": proposal_id,
                        "domain": domain,
                        "status": "error",
                        "error": "adapter quality gate: stub adapter after retry — not staged",
                        "quality_issues": _issues2,
                        "files_written": [],
                    }
            else:
                errors.extend([f"adapter_quality_hard_fail: {i}" for i in _issues])
                return {
                    "id": proposal_id,
                    "domain": domain,
                    "status": "error",
                    "error": "adapter quality gate: stub adapter, retry unavailable — not staged",
                    "quality_issues": _issues,
                    "files_written": [],
                }

    # ----- Step 3: Write toolchain directory structure ---------------------
    log.info(f"[forge] writing toolchain to: {out_dir}")

    try:
        # Directory structure
        (out_dir / "contracts" / "catalogs").mkdir(parents=True)
        (out_dir / "adapters" / "ssh_collect").mkdir(parents=True)
        (out_dir / "projections" / domain).mkdir(parents=True)

        # VERSION
        vf = out_dir / "VERSION"
        vf.write_text("0.1.0-forge\n")
        files_written.append("VERSION")

        # Catalog
        cat_name = f"attack_preconditions_catalog.{domain}.v1.json"
        cat_path = out_dir / "contracts" / "catalogs" / cat_name
        cat_path.write_text(json.dumps(catalog, indent=2))
        files_written.append(f"contracts/catalogs/{cat_name}")

        # Adapter __init__.py
        init_path = out_dir / "adapters" / "ssh_collect" / "__init__.py"
        init_path.write_text("")
        files_written.append("adapters/ssh_collect/__init__.py")

        # Adapter parse.py
        adapter_path = out_dir / "adapters" / "ssh_collect" / "parse.py"
        adapter_path.write_text(adapter_code)
        files_written.append("adapters/ssh_collect/parse.py")

        # Projector __init__.py
        proj_init = out_dir / "projections" / domain / "__init__.py"
        proj_init.write_text("")
        files_written.append(f"projections/{domain}/__init__.py")

        # Projector run.py — boilerplate template
        safe_domain = domain.replace("-", "_")
        projector_code = PROJECTOR_TEMPLATE.format(
            domain=domain,
            safe_domain=safe_domain,
            domain_label=domain.replace("_", " ").replace("-", " ").title(),
            generated_at=datetime.now(timezone.utc).isoformat(),
        )
        proj_path = out_dir / "projections" / domain / "run.py"
        proj_path.write_text(projector_code)
        files_written.append(f"projections/{domain}/run.py")

        # Forge metadata
        meta = {
            "domain": domain,
            "description": description,
            "attack_surface": gap.get("attack_surface", ""),
            "hosts": gap.get("hosts", []),
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "generation_backend": generation_backend,
            "errors": errors,
        }
        (out_dir / "forge_meta.json").write_text(json.dumps(meta, indent=2))
        files_written.append("forge_meta.json")

    except Exception as exc:
        errors.append(f"File write error: {exc}")
        return {"success": False, "domain": domain, "errors": errors}

    return {
        "success": True,
        "domain": domain,
        "tc_name": tc_name,
        "staging_path": str(out_dir),
        "catalog": catalog,
        "wicket_count": len(catalog.get("wickets", {})),
        "path_count": len(catalog.get("attack_paths", {})),
        "files_written": files_written,
        "generation_backend": generation_backend,
        "errors": errors,
    }


def install_toolchain(staging_path: str | Path,
                      install_dir: Path | None = None) -> dict:
    """
    Install a staged toolchain into the main SKG directory.
    Returns installation metadata.
    """
    staging_path = Path(staging_path)
    install_dir = install_dir or SKG_HOME
    dest = install_dir / staging_path.name
    result = {
        "installed_path": str(dest),
        "installed": False,
        "preserved_existing": False,
    }

    staged_catalogs = list(staging_path.glob("contracts/catalogs/*.json"))
    existing_catalogs = list(dest.glob("contracts/catalogs/*.json")) if dest.exists() else []
    if staged_catalogs and existing_catalogs:
        try:
            staged = json.loads(staged_catalogs[0].read_text())
            existing = json.loads(existing_catalogs[0].read_text())
            staged_wickets = staged.get("wickets", {}) or {}
            existing_wickets = existing.get("wickets", {}) or {}
            staged_paths = staged.get("attack_paths", {}) or {}
            existing_paths = existing.get("attack_paths", {}) or {}

            staged_score = (len(staged_wickets), len(staged_paths))
            existing_score = (len(existing_wickets), len(existing_paths))
            has_new_ids = bool(
                set(staged_wickets) - set(existing_wickets)
                or set(staged_paths) - set(existing_paths)
            )

            # Refuse to overwrite a stronger active toolchain with a weaker
            # staged draft. Keep the accepted record, but preserve the active
            # runtime substrate until a non-regressive merge/install exists.
            if staged_score < existing_score and not has_new_ids:
                log.warning(
                    "[forge] refusing regressive install: %s weaker than %s",
                    staging_path, dest,
                )
                result["preserved_existing"] = True
                return result
        except Exception as exc:
            log.warning(f"[forge] install comparison failed: {exc}")

    if dest.exists():
        # Backup existing active tree before merging. Do not replace the whole
        # toolchain; staged forge output usually only contains generated
        # catalogs/projections and should not strip live adapters/runtime code.
        backup = dest.with_name(dest.name + ".backup")
        if backup.exists():
            shutil.rmtree(backup)
        shutil.copytree(dest, backup)
        shutil.copytree(staging_path, dest, dirs_exist_ok=True)
    else:
        shutil.copytree(staging_path, dest)
    log.info(f"[forge] installed: {dest}")
    result["installed"] = True

    # Install Python dependencies declared by the toolchain (if any).
    # Generated toolchains may emit a requirements.txt so they can pull in
    # domain-specific libraries (e.g. impacket for AD, paramiko for SSH).
    req_file = dest / "requirements.txt"
    if req_file.exists():
        try:
            import subprocess, sys
            proc = subprocess.run(
                [sys.executable, "-m", "pip", "install", "-r", str(req_file), "--quiet"],
                capture_output=True, text=True, timeout=120,
            )
            if proc.returncode == 0:
                log.info(f"[forge] dependencies installed from {req_file}")
            else:
                log.warning(f"[forge] pip install failed for {req_file}: {proc.stderr[:200]}")
        except Exception as exc:
            log.warning(f"[forge] dependency install error: {exc}")

    # Load through the shared projector discovery path so install-time
    # activation matches runtime activation.
    try:
        from skg_services.gravity.projector_runtime import (
            _discover_toolchain_projector,
            _projector_cache,
        )

        tc_name = staging_path.name
        _projector_cache.pop(tc_name, None)
        if _discover_toolchain_projector(tc_name):
            log.info(f"[forge] registered projector: {tc_name}")
        else:
            log.warning(f"[forge] projector discovery failed: {tc_name}")
    except Exception as exc:
        log.warning(f"[forge] projector registration failed: {exc}")

    # Notify registered post-install hooks (e.g. wicket graph re-registration).
    for _hook in list(_post_install_hooks):
        try:
            _hook(dest)
        except Exception as _he:
            log.debug(f"[forge] post-install hook error: {_he}")

    return result


