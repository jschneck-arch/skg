def _check_adapter_quality(code: str, domain: str) -> tuple[bool, list[str]]:
    """
    Detect placeholder patterns that indicate the adapter is not functional.
    Returns (is_acceptable, list_of_issues).
    """
    issues = []
    lines = code.splitlines()

    # Count TODO lines and stub patterns
    todo_lines = [i+1 for i, l in enumerate(lines)
                  if "# TODO" in l or "# todo" in l.lower()]
    stub_lines = [i+1 for i, l in enumerate(lines)
                  if ("Replace with" in l or "Replace With" in l)
                  and ("unknown" in l or "realized" in l or "blocked" in l)]

    # Check: both branches of if/else returning "unknown"
    same_branch_stubs = 0
    for i in range(len(lines) - 2):
        l_stripped = lines[i].replace(" ", "").replace('"', "'")
        if "status='unknown'" in l_stripped:
            for j in range(i+1, min(i+4, len(lines))):
                lj_stripped = lines[j].replace(" ", "").replace('"', "'")
                if "status='unknown'" in lj_stripped:
                    same_branch_stubs += 1
                    break

    if todo_lines:
        issues.append(f"TODO markers at lines {todo_lines[:5]}")
    if stub_lines:
        issues.append(f"Placeholder 'Replace with' comments at lines {stub_lines[:5]}")
    if same_branch_stubs >= 2:
        issues.append(f"Both if/else branches return 'unknown' in {same_branch_stubs} check functions — no real parsing logic")

    is_acceptable = len(issues) == 0
    return is_acceptable, issues


def _fix_adapter(code: str, domain: str, catalog: dict) -> str:
    """Fix common generation issues in adapter code."""
    # Ensure TOOLCHAIN and SOURCE_ID are correct
    tc = f'skg-{domain}-toolchain'
    sid = f'adapter.{domain}_collect'

    if f'TOOLCHAIN = "skg-' not in code and 'TOOLCHAIN' not in code:
        code = f'TOOLCHAIN = "{tc}"\nSOURCE_ID = "{sid}"\n\n' + code
    else:
        # Fix any wrong toolchain name
        code = re.sub(r'TOOLCHAIN\s*=\s*"[^"]*"', f'TOOLCHAIN = "{tc}"', code)
        code = re.sub(r'SOURCE_ID\s*=\s*"[^"]*"', f'SOURCE_ID = "{sid}"', code)

    # Ensure emit function is present — copy from template if missing
    if 'def emit(' not in code:
        code = _EMIT_TEMPLATE + '\n\n' + code

    # Ensure iso_now is present
    if 'def iso_now' not in code:
        code = 'def iso_now():\n    from datetime import datetime, timezone\n    return datetime.now(timezone.utc).isoformat()\n\n' + code

    # Ensure get_version is present
    if 'def get_version' not in code:
        code = ('def get_version():\n    try:\n'
                '        return (Path(__file__).resolve().parents[2] / "VERSION").read_text().strip()\n'
                '    except Exception:\n        return "0.0.0"\n\n') + code

    # Ensure imports
    needed_imports = ['import json', 'import uuid', 'from pathlib import Path',
                      'from datetime import datetime, timezone']
    existing = code[:500]
    prepend = ''
    for imp in needed_imports:
        if imp not in existing:
            prepend += imp + '\n'
    if prepend:
        code = prepend + '\n' + code

    return code


