def _load_example_adapters(n: int = 2) -> list[str]:
    """Load N existing adapter parse.py files as generation examples."""
    examples = []
    for tc_dir in sorted(SKG_HOME.glob("skg-*-toolchain")):
        for parse_py in tc_dir.glob("adapters/*/parse.py"):
            try:
                text = parse_py.read_text(errors="replace")
                if len(text) > 200:
                    examples.append(text)
                    if len(examples) >= n:
                        return examples
            except Exception:
                pass
    return examples


